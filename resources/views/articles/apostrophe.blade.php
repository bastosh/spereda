    <p>
        Comment rendre l’apostrophe typographique ? Si l’informatique avait été inventée par des typographes, nul doute
        que l’apostrophe aurait (avec l’espace fine insécable) sa place sur les claviers. Mais ces derniers sont ce qu’ils
        sont et il faut composer (je pèse le mot) avec.
    </p>
    <p>
        Sur Mac, l’apostrophe typographique s’obtient par la combinaison <kbd>alt</kbd> <kbd>maj</kbd> <kbd>'</kbd>.
    </p>
    <p>
        Sur PC, avec la combinaison <kbd>alt</kbd> <kbd>0</kbd> <kbd>1</kbd> <kbd>4</kbd> <kbd>6</kbd>.
    </p>
    <p>
        En 2016, un projet de norme a été confié à l’AFNOR par le ministère de la Culture et de la Communication.
    </p>
    <p>
        En attendant que le clavier français idéal fasse son chemin jusqu’à nous, prenez de bonnes habitudes :
        faites œuvre de santé typographique et répandez l’apostrophe, la vraie. La qualité de votre communication
        s’en trouvera améliorée.
    </p>
