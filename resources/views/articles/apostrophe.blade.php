    <p>
        L’apostrophe est le signe typographique en forme de virgule qui, en français, sert à marquer l’élision (car, c’est bien connu,
        on ne dit point la élision). En forme de virgule, oui. Et c’est normal, car, comme le rappelle Jean-Pierre Lacroux, à l’origine
        « l’apostrophe n’est qu’une virgule libérée de la pesanteur qui la clouait sur la ligne de base ». Dans toute bonne police de
        caractères qui se respecte, il y a donc une belle correspondance entre la virgule et l’apostrophe.
    </p>
    <p>
        Tout irait pour le mieux dans le meilleur des mondes typographiques si l’apostrophe, que nous utilisons quand même de manière
        régulière (je vous mets au défi de vous en priver) existait sur nos claviers.  Mais qu’est-ce que tu nous chantes, c’est juste là,
        la touche <kbd>4</kbd> , allez-vous répondre en chœur. Non. Ce petit trait vertical, que l’on appelle parfois  apostrophe dactylographique ,
        ou  apostrophe droite , mais que je vous invite à appeler désormais  chiure de mouche , n’est pas une apostrophe. Typographiquement,
        ce n’est rien. C’est un signe qui va nous servir, lorsque nous codons, à délimiter des chaînes de caractères. Mais c’est tout.
    </p>
    <p>
        À ce propos, vous lirez souvent sur le Web que pour utiliser une apostrophe à l’intérieur de guillemets simples (qui ne sont
        en aucun cas des guillemets, mais on pourra y revenir dans un autre article), il faut utiliser un caractère d’échappement :
    </p>
    <pre>
                        <code>
 var message = 'Je n\'aime pas les apostrophes';</code>
                    </pre>
    <p>
        Typographiquement, c’est un non-sens. Pourquoi résoudre un problème que l’on n’aurait pas en utilisant une véritable apostrophe :
    </p>
    <pre>
                        <code>
 var message = 'J’aime les apostrophes';</code>
                    </pre>
    <p>
        Lorsque vous êtes dans un logiciel de traitement de texte et que vous saisissez du texte, une opération invisible en
        arrière-boutique remplace le vilain signe droit par une belle apostrophe, vous maintenant en secret dans l’illusion
        que les apostrophes vous sont acquises. Mais que se passe-t-il lorsque vous écrivez un e-mail ou que vous écrivez du
        code dans votre éditeur de texte préféré ? Rien. La magie n’opère pas et c’est pour cette raison que le Web est truffé
        de chiures de mouche.
    </p>
